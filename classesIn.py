class calculator:

    def addition(x, y):
        added = x + y
        print(added)
    def subtraction(x, y):
        subtracted = x - y
        print(subtracted)
    def multiplication(x, y):
        multi = x * y
        print(multi)
    def division(x, y):
        divi = x / y
        print(divi)        

# To access a function in a class
calculator.multiplication(5, 9)